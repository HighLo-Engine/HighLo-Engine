/**
 * =========================================================================================================
 *													API usage
 * =========================================================================================================
 *
 * Example #1:
 * -----------
 *
 * Ref<DocumentWriter> writer = DocumentWriter::Create("<filepath>");
 * writer->WriteString("Hello", "World");
 * writer->WriteUint64("number", 64);
 * writer->WriteOut();
 *
 * -> should result in XML:
 * <?xml version="1.0.0">
 * <HighLo version="1.0.0">
 *   <string key="Hello">World</string>
 *   <uint64 key="number">64</uint64>
 * </HighLo>
 * 
 * Example #2:
 * -----------
 *
 * Ref<DocumentWriter> writer = DocumentWriter::Create("<filepath>");
 * writer->BeginObject();
 * writer->WriteString("Hello", "World");
 * writer->WriteUInt64("number", 64);
 * writer->EndObject();
 * writer->WriterOut();
 *
 * -> should result in XML:
 * <?xml version="1.0.0">
 * <HighLo version="1.0.0">
 *   <string key="Hello">World</string>
 *   <uint64 key="number">64</uint64>
 * </HighLo>
 *
 * Example #3:
 * -----------
 *
 * Ref<DocumentWriter> writer = DocumentWriter::Create("<filepath>");
 * writer->BeginArray();
 * writer->WriteString("Hello", "World");
 * writer->WriteUInt64("number", 64);
 * writer->EndArray();
 * writer->WriteOut();
 *
 * -> should result in XML:
 * <?xml version="1.0.0">
 * <HighLo version="1.0.0">
 *   <array>
 *     <string>World</string>
 *     <uint64>64</uint64>
 *   </array>
 * </HighLo>
 *
 * Example #4:
 * -----------
 *
 * Ref<DocumentWriter> writer = DocumentWriter::Create("<filepath>");
 * writer->BeginArray();
 * for ()
 *   writer->BeginObject();
 *   writer->WriteString("Hello", "World");
 *   writer->WriteUInt64("number", 64);
 *   writer->EndObject();
 * endfor
 * writer->EndArray();
 * writer->WriteOut();
 *
 * -> should result in XML:
 * <?xml version="1.0.0">
 * <HighLo version="1.0.0">
 *   <arraymap>
 *     <string key="Hello">World</string>
 *     <uint64 key="number">64</uint64>
 *   </arraymap>
 * </HighLo>
 */

